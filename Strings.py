"""
Examples to show how strings works in python

Sequence of characters
Contains a-z , 0-9, @
In double or single quotes
"""

a = "This is a simple string"
b = 'Using single quotes'

# Will print string from variable a
print(a)
# Will print string from variable b
print(b)

# Example on how to insert quotes in a string
c = "Need to use 'quotes' inside a string"
print(c)

# Another example of how to input quotes in string . Used backlash to print quotes
d = "Another way to handle \"quotes\""
print(d)

# Example on how to separate a large string with a backlash
a = ("This is a single\
   string")

print(a)
